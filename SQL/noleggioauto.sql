Create TABLE Filiale (
    IdFiliale SERIAL PRIMARY KEY,
    Telefono CHAR(13) NOT NULL,
    Citta VARCHAR(50) NOT NULL,
    Via VARCHAR(50) NOT NULL,
    Numero VARCHAR(5) NOT NULL,
    CAP VARCHAR(5) NOT NULL
);

Create TABLE Cliente (
    IdCliente SERIAL PRIMARY KEY,
    Email VARCHAR(50) NOT NULL,
    Telefono CHAR(13) NOT NULL,
    Citta VARCHAR(50) NOT NULL,
    Via VARCHAR(50) NOT NULL,
    Numero VARCHAR(5) NOT NULL,
    CAP VARCHAR(5) NOT NULL
);

Create TABLE Tariffario (
    Marca VARCHAR(20),
    Modello VARCHAR(50),
    TariffaBase NUMERIC(10,2) NOT NULL,
    PRIMARY KEY (Marca, Modello),
    CHECK (TariffaBase >= 0)
);

CREATE TYPE TipologiaAlimentazione as ENUM ('BENZINA', 'DIESEL', 'ELETTRICO', 'IBRIDO', 'GPL');
CREATE TYPE TipologiaStato as ENUM ('DISPONIBILE', 'NOLEGGIATO', 'MANUTENZIONE');

Create TABLE Veicolo(
    Targa CHAR(7) primary key,
    Chilometraggio INT NOT NULL,
    AnnoImmatricolazione DATE NOT NULL,
    Potenza INT NOT NULL,
    TipoAlimentazione TipologiaAlimentazione NOT NULL,
    Stato TipologiaStato NOT NULL,
    TariffarioMarca VARCHAR (20) NOT NULL,
    TariffarioModello VARCHAR (50) NOT NULL,
    Filiale INT NOT NULL REFERENCES Filiale(idFiliale),
    FOREIGN KEY (TariffarioMarca, TariffarioModello) REFERENCES Tariffario (Marca, Modello),
    CHECK(Chilometraggio >= 0 and Potenza > 0)
);

CREATE TYPE TipologiaVeicolo as ENUM ('SPORTIVA', 'UTILITARIA', 'STATION WAGON', 'BERLINA', 'SUV', 'CABRIOLET', 'PICK-UP');

Create TABLE Automobile(
    Veicolo char(7) primary key,
    Tipologia TipologiaVeicolo NOT NULL,
    NumeroPorte INT NOT NULL,
    FOREIGN KEY (Veicolo) REFERENCES Veicolo(targa) on delete cascade,
    CHECK (NumeroPorte >= 2 and NumeroPorte <= 5)
);

Create TABLE Furgone(
    Veicolo char(7) primary key,
    Volume INT NOT NULL,
    PortataUtile INT NOT NULL,
    FOREIGN KEY (Veicolo) REFERENCES Veicolo(targa) on delete cascade
);

CREATE TYPE TipologiaMoto as ENUM ('NAKED','SUPERSPORTIVA', 'ENDURO', 'TOURER', 'CRUISER', 'SCOOTER', 'CICLOMOTORI');

Create TABLE Moto(
    Veicolo char(7) primary key,
    Tipologia TipologiaMoto NOT NULL,
    FOREIGN KEY (Veicolo) REFERENCES Veicolo(targa) on delete cascade
);

CREATE Table Contratto(
    IdContratto SERIAL PRIMARY KEY,
    ChilometriIniziali INT NOT NULL,
    Canone NUMERIC(10,2) NOT NULL,
    DataInizio DATE NOT NULL,
    DataFine DATE NOT NULL,
    ChilometriFinali INT,
    Veicolo char(7) REFERENCES Veicolo(Targa),
    Cliente INT REFERENCES Cliente(IdCliente),
    Filiale INT REFERENCES Filiale(IdFiliale),
    CHECK (DataInizio <= DataFine and ChilometriIniziali <= ChilometriFinali),
    CHECK (Canone >= 0)
);

Create TABLE Pagamento(
    Contratto INT REFERENCES Contratto(IdContratto) on delete cascade,
    DataPagamento DATE NOT NULL,
    Importo NUMERIC(10,2) NOT NULL,
    primary key (Contratto, DataPagamento),
    CHECK(Importo > 0)
);

CREATE TYPE TipologiaAssicurazione as ENUM ('RCA', 'FURTO', 'INCENDIO', 'CRISTALLI', 'KASKO', 'EVENTI NATURALI', 'ASSISTENZA STRADALE', 'ATTI VANDALICI');

Create TABLE Assicurazione(
    Tipologia TipologiaAssicurazione primary key,
    TariffaGiornaliera NUMERIC(10,2) NOT NULL,
    Massimale NUMERIC(10,2) NOT NULL
);

Create TABLE Copertura(
    Assicurazione TipologiaAssicurazione references Assicurazione(Tipologia),
    Contratto INT references Contratto(idcontratto) on delete cascade,
    primary key(Assicurazione, Contratto) 
);

CREATE TYPE TipologiaManutenzione as ENUM ('ORDINARIA','STRAORDINARIA');

Create TABLE Manutenzione (
    IdManutenzione SERIAL primary key,
    DataInizio DATE NOT NULL,
    DataFine DATE,
    Tipologia TipologiaManutenzione NOT NULL,
    Veicolo CHAR(7) NOT NULL REFERENCES Veicolo(Targa),
    CHECK (DataInizio <= DataFine)
);

Create TABLE Fattura (
    Manutenzione INT PRIMARY KEY REFERENCES Manutenzione(idManutenzione) on delete cascade,
    Fornitore VARCHAR(100) NOT NULL,
    DataFattura DATE NOT NULL,
    Importo NUMERIC(10,2) NOT NULL
);

Create TABLE Privato (
    IdCliente INT PRIMARY KEY,
    CodiceFiscale VARCHAR(16) UNIQUE NOT NULL,
    Nome VARCHAR(50) NOT NULL,
    Cognome VARCHAR (50) NOT NULL,
    FOREIGN KEY (IdCliente) REFERENCES Cliente(IdCliente) on delete cascade
);

Create TABLE Azienda (
    IdCliente INT PRIMARY KEY,
    PartitaIva VARCHAR(11) UNIQUE NOT NULL,
    RagioneSociale VARCHAR(50) NOT NULL,
    FOREIGN KEY (IdCliente) REFERENCES Cliente(IdCliente) on delete cascade
);

