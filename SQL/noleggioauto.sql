-- Script di update per assegnare il valore a canone in contratto:
CREATE VIEW Contratto_SommaPrezziAssicurazioni (idContratto, TotalePrezzoAssicurazione) AS(
	SELECT  cop.Contratto , sum (A.TariffaGiornaliera) as CostoGiornalieroTariffa
	FROM (CONTRATTO Con JOIN COPERTURA Cop ON Con.idContratto = Cop.Contratto) JOIN Assicurazione A on Cop.Assicurazione = A.Tipologia
	GROUP BY Cop.Contratto
);

CREATE VIEW Contratto_TariffaVeicolo (idContratto, TariffaVeicolo) AS(
	SELECT  C.idContratto , T.tariffaBase
	FROM (CONTRATTO C JOIN VEICOLO V ON C.Veicolo = V.targa) JOIN Tariffario T on  T.Marca= V.TariffarioMarca and T.Modello = V.TariffarioModello
);

UPDATE Contratto set Canone= (
SELECT ((A.TotalePrezzoAssicurazione + T.TariffaVeicolo)*(Contratto.datafine-Contratto.datainizio + 1))
FROM (Contratto_SommaPrezziAssicurazioni  A JOIN Contratto_TariffaVeicolo T on A.idContratto = T.idContratto)
WHERE Contratto.idContratto = A.idContratto
);

